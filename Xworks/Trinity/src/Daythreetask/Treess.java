package Daythreetask;

public class Treess {

	public static void main(String[] args) {

		int i, j, row = 5;       
		//Outer loop  for rows  
		for (i=0; i<row; i++)   
		{  
		//inner loop  for space      
		for (j=row-i; j>1; j--)   
		{  
		//prints space between two stars  
		System.out.print(" ");   
		}   
		//inner loop for columns  
		for (j=0; j<=i; j++ )   {   
			
				
			
		//prints number  
			
			System.out.print("1 ");   
			
		}   
		
		System.out.println();   
		}   
		}   
		
		
	}



