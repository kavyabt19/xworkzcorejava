class TimePrinter{

public static void main(String args[]){

	   byte time;
	   time = 8;
	   System.out.println(" ");
	   System.out.println(    " "  +time+"AM"  );
	   System.out.println(" ");
	   time = 8;
	   System.out.println("  Exactly  at "+time+ "AM we start X-works java class");
	   System.out.println("  ");
	   time = 10;
	   System.out.println("  Around at "+time+ "AM we wind up X-works java class");
	   }
	   
	   
}	   